package JavaClass1;

public class AssignmentOperator {

	public static void main(String[] args) {
           int a=10;// Local Variable
           a +=3;// Do not pass space between + and a=
           System.out.println(a);
           a -=4;// Do not pass space between - and a=
           System.out.println(a);
           a *=2;// Do not pass space between * and a=
           System.out.println(a);
           a /=2;// 
           System.out.println(a);
           
	}

}
